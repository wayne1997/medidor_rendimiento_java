    private long readFromInputSize() {
        try{
            long inputSize = ((Number) this.sizeInputLabel.getValue()).longValue();
            return inputSize;
        }catch(Exception ex){
            this.writeOnConsole(ex.getMessage());
            System.out.println("Ha fallado");
            return 0;
        }
    }
    
    private void writeOnConsole(String text){
        StyledDocument doc = this.consoleArea.getStyledDocument();
        var myStyle =  this.consoleArea.addStyle("miEstilo", null);
        StyleConstants.setForeground(myStyle, Color.GREEN);
        try{
            doc.insertString(doc.getLength(), text, myStyle);
            doc.insertString(doc.getLength(), "\n", myStyle);
        }catch (BadLocationException e) {
            e.printStackTrace();
        }
    }
    
    private void generate2DGraph(List<Long> xData, List<Long> yData){        
        this.chart = new XYChartBuilder()
                .title("An√°lisis de rendimiento (Tiempo-Tama√±o)")
                .height(350)
                .width(650)
                .xAxisTitle("Cantidad de elementos")
                .yAxisTitle("Tiempo de ejecuci√≥n")
                .build();
        this.chart.addSeries("Algoritmo A", xData, yData);
        
        //Crea una imagen que agrega a un label para poder graficar
         BufferedImage image = BitmapEncoder.getBufferedImage(this.chart);
         JLabel label = new JLabel(new ImageIcon(image));
         
         this.graphPanel.removeAll();
         this.graphPanel.setLayout(new BorderLayout());
         this.graphPanel.add(label, BorderLayout.CENTER);
         this.graphPanel.revalidate();
         this.graphPanel.repaint();         
    }
    
    
