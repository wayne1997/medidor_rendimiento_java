    private void simpleLoopAlgBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpleLoopAlgBtnActionPerformed

        try {
            long startTime = System.currentTimeMillis();
            long inputSize = this.readFromInputSize();
            long total = this.baseAlgorithms.totalSumLoopAlg(inputSize);
            this.writeOnConsole("El total es: " + total);
            long endTime = System.currentTimeMillis();
            long avgTime = endTime - startTime;
            this.writeOnConsole("El tiempo en milisegundos segundos es: " + avgTime);
            this.codeEditorPanel.setText(this.baseAlgorithms.getTotalSumLoopAlg());
            this.sizeInputLabel.setValue(0);
            xData.add(inputSize);
            yData.add(avgTime);
            this.generate2DGraph(xData, yData);
        }catch(Exception ex) {
            System.out.println("El resultado es: " + ex.getMessage());
        }
    }//GEN-LAST:event_simpleLoopAlgBtnActionPerformed

    private void algeSumAlgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_algeSumAlgActionPerformed
        try{
            long startTime = System.currentTimeMillis();
            long inputSize = this.readFromInputSize();
            long total = this.baseAlgorithms.algebraicSumAlg(inputSize);
            long endTime = System.currentTimeMillis();
            String result = "El resultado algebraico es: " + total;
            this.codeEditorPanel.setText(this.baseAlgorithms.getTotalSumAlgebraicAlg());
            this.writeOnConsole( result );
            long avgTime = endTime - startTime;
            xData.add(inputSize);
            yData.add(avgTime);
            this.generate2DGraph(xData, yData);
            this.sizeInputLabel.setValue(0);
        }catch(Exception exception) {
            throw exception;
        }
                
    }//GEN-LAST:event_algeSumAlgActionPerformed

    private void doubleLoopAlgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doubleLoopAlgActionPerformed
        
        long inputSize = this.readFromInputSize();
        long startTime = System.currentTimeMillis();
        String result = this.baseAlgorithms.CountBottomToTop(inputSize);
        this.codeEditorPanel.setText(this.baseAlgorithms.getDoubleLoopAlg());
        this.writeOnConsole(result);
        long endTime = System.currentTimeMillis();
        long avgTime = endTime - startTime;
        xData.add(inputSize);
        yData.add(avgTime);
        this.generate2DGraph(xData, yData);
        this.sizeInputLabel.setValue(0);
    }//GEN-LAST:event_doubleLoopAlgActionPerformed

